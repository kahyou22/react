package ch02_데이터와변수;

/**
 * 정수
 * 
 * 1. byte: 1byte
 * 
 * 2. short / char: 2byte
 * 
 * 3. int: 4byte
 * 
 * 4. long: 8byte
 */
public class Ex04_long정수 {

	public static void main(String[] args) {

		long var1 = -128;
		long var2 = -30;
		long var3 = 0;
		long var4 = 30;
		long var5 = 127;

		System.out.println("var1: " + var1);
		System.out.println("var2: " + var2);
		System.out.println("var3: " + var3);
		System.out.println("var4: " + var4);
		System.out.println("var5: " + var5);
	}

}
